public class Ejercicio5 {

    public static void main(String[] args) {
        int i=1;
        //Hacemos el bucle While
        while (i<=100) {
            if (i%2==0){
                System.out.println("El número " + i+" es par");
            }
            else {
            System.out.println("El número " + i+" es impar");
            }
            i++;
        }
    }
    
}
